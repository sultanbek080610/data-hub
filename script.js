function openPage(page) {
    const content = document.getElementById("content");

    if (page === "about") {
        content.innerHTML = `
            <h2>Об университете</h2>
            <p>Информация о миссии, истории, лидерстве и достижениях университета.</p>
        `;
    }

    if (page === "programs") {
        content.innerHTML = `
            <h2>Академические программы</h2>
            <p>Перечень и подробная информация о предлагаемых учебных программах и курсах.</p>
        `;
    }

    if (page === "admission") {
        content.innerHTML = `
            <h2>Приём и поступление</h2>
            <p>Требования к поступлению, сроки подачи, стипендии и финансовая помощь.</p>
        `;
    }

    if (page === "tour") {
        content.innerHTML = `
            <h2>3D-тур</h2>
            <p>Виртуальный 3D-тур по университетскому кампусу.</p>
        `;
    }

    if (page === "international") {
        content.innerHTML = `
            <h2>Международное сотрудничество</h2>
            <p>Партнерские университеты, обменные программы и возможности для иностранных студентов.</p>
        `;
    }

    if (page === "compare") {
        content.innerHTML = `
            <h2>Функция сравнения</h2>
            <p>Сравнение вузов и их академических программ по различным параметрам.</p>
        `;
    }
}
